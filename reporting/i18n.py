def labels(language: str | None) -> dict[str, str]:
    return LABELS[normalize_language(language)]


def pattern_display(pattern_key: str, language: str | None = "en") -> str:
    parts = str(pattern_key).split("_", 1)
    name = parts[1] if len(parts) > 1 else str(pattern_key)
    if normalize_language(language) == "kn":
        return PATTERN_NAMES_KN.get(name, name.replace("_", " "))
    return name.replace("_", " ").title()


