SUPPORTED_LANGUAGES = {"en", "kn"}


def normalize_language(language: str | None) -> str:
    code = str(language or "en").strip().lower()
    if code in {"kannada", "ಕನ್ನಡ"}:
        return "kn"
    return code if code in SUPPORTED_LANGUAGES else "en"


