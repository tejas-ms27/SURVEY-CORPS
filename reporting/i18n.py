def kannada_account_overview(account_id: str, score: float, strong_n: int, distinct_n: int) -> str:
    return (
        f"{account_id} ಖಾತೆಯಲ್ಲಿ {strong_n} ಬಲವಾದ ಮಾದರಿ ಪ್ರಕಾರ(ಗಳು) ಮತ್ತು "
        f"{distinct_n} ಪ್ರತ್ಯೇಕ ಮಾದರಿ(ಗಳು) ಕಂಡುಬಂದಿವೆ. ಒಟ್ಟು ಸಂಯುಕ್ತ ಸಂದೇಹ ಅಂಕ {round(score)} ಆಗಿದೆ."
    )


def kannada_case_summary(cluster_count: int, suspicious_count: int, top_cluster: str, top_score: float) -> str:
    if not cluster_count:
        return "ಈ ರನ್‌ನಲ್ಲಿ ವ್ಯವಹಾರಾಧಾರಿತ ಖಾತೆ ಕ್ಲಸ್ಟರ್‌ಗಳು ಲಭ್ಯವಿಲ್ಲ."
    return (
        f"ಪ್ರಕರಣದಲ್ಲಿ {cluster_count} ಖಾತೆ ಕ್ಲಸ್ಟರ್(ಗಳು) ಇವೆ ಮತ್ತು "
        f"{suspicious_count} ಖಾತೆ(ಗಳು) ಸಂದೇಹಾಸ್ಪದ ಚಟುವಟಿಕೆಗಾಗಿ ಶ್ರೇಯಾಂಕಗೊಂಡಿವೆ. "
        f"ಅತ್ಯುನ್ನತ ಆದ್ಯತೆಯ ಕ್ಲಸ್ಟರ್ {top_cluster} ಆಗಿದ್ದು, ಒಟ್ಟು ಅಂಕ {top_score:.2f}."
    )
